    private void formWindowOpened(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowOpened

        Thread trCar = new Thread() {
            int i = 0;

            // toda e qualquer outra rotina que quero que a Thread faça uso o método run
            @Override
            public void run() {
                while (i < 100) {

                    i++;
                    carregandoTxt.setText(String.valueOf(i) + "%");
                    progressoBar.setValue(i);

                    try {
                        sleep(100);//intervalo para execução do método run

                    } catch (Exception e) {
                    }
                }//fecha o WHILE

                dispose();

                //instanciando e setando a visualização de um JFrame Form
                new frmLogin().setVisible(true);

            }
        };
        trCar.start();//executando a Thread


    }//GEN-LAST:event_formWindowOpened

